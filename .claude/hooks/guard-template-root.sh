#!/usr/bin/env bash
# guard-template-root.sh - UserPromptSubmit-хук.
#
# Назначение: запретить запуск клиентских скилов (slash-команд) В САМОМ ШАБЛОНЕ
# (seo-pipeline-template), чтобы случайный /seo-shablon, /seo-statya или
# /handoff-process не загадил шаблон клиентскими артефактами. Реальный инцидент:
# /handoff-process по ошибке применил клиента в шаблон вместо проекта.
#
# Детектор шаблона: untracked-маркер .claude/.is-template-root.
#   - В шаблоне (этот рабочий каталог) маркер ЕСТЬ -> команды блокируются.
#   - У клиентов (git clone) маркера НЕТ (он gitignored, в клон не попадает)
#     -> хук всегда exit 0, никак не мешает. Это fail-safe: нет маркера = разрешено.
#
# Разрешены даже в шаблоне: /guide (read-only справка) и /sync-from-template
# (служебная синхронизация машинерии). Обычные (не slash) промпты - всегда разрешены.
#
# Семантика UserPromptSubmit: exit 2 + stderr -> промпт блокируется, stderr виден
# пользователю. Любая внутренняя ошибка -> exit 0 (fail-open), чтобы не сломать сессию.

set -u

# Корень проекта = два уровня вверх от .claude/hooks/ (не зависит от cwd).
SCRIPT_DIR=$(cd "$(dirname "$0")" 2>/dev/null && pwd) || exit 0
ROOT=$(cd "${SCRIPT_DIR}/../.." 2>/dev/null && pwd) || exit 0
MARKER="${ROOT}/.claude/.is-template-root"

# Нет маркера -> это не шаблон (клиентский клон) -> ничего не блокируем.
[ -f "${MARKER}" ] || exit 0

INPUT="$(cat - 2>/dev/null || true)"
[ -n "${INPUT}" ] || exit 0

# Вытащить значение поля "prompt" без jq (его нет в окружении).
# Берём всё после первого "prompt":" и обрезаем по следующей кавычке.
# Для детекции ведущей slash-команды этого достаточно: команда идёт до любых кавычек.
prompt=$(printf '%s' "${INPUT}" \
  | sed -n 's/.*"prompt"[[:space:]]*:[[:space:]]*"//p' \
  | sed 's/".*//' \
  | tr -d '\r')

# Снять ведущие пробелы.
prompt="${prompt#"${prompt%%[![:space:]]*}"}"

# Не slash-команда -> обычный промпт -> разрешаем.
case "${prompt}" in
  /*) ;;
  *) exit 0 ;;
esac

# Имя команды: символы после "/" до первого нелитерала.
cmd=$(printf '%s' "${prompt}" | sed -E 's#^/([A-Za-z0-9_-]+).*#\1#')

# Белый список разрешённых в шаблоне.
case "${cmd}" in
  guide|sync-from-template) exit 0 ;;
esac

# Всё остальное в шаблоне - блок.
cat >&2 <<EOF
🚫 Это рабочий каталог ШАБЛОНА (seo-pipeline-template), а не клиентский проект.
Клиентская команда /${cmd} здесь отключена, чтобы не загрязнить шаблон.

Что делать:
  - Для работы с клиентом открой ~/seo-projects/<slug>/ в отдельной сессии и запусти команду там.
  - В шаблоне разрешены только /guide и /sync-from-template.
  - Машинерию (скилы, агенты, хуки, скрипты) правь обычными инструментами (Edit/Write), без slash-команд.

(Защита включается маркером .claude/.is-template-root. В клиентских клонах его нет - там команды работают как обычно.)
EOF
exit 2
